    private void Adicionar() throws SQLException {
        if (tfAno.getText().trim().length() > 0 && tfAno.getText().trim().length() > 0 && tfAno.getText().trim().length() > 0) {
            Veiculo v = new Veiculo();
            v.setCodigo(veiculoDAO.getLastId());
            v.setNr_ano(Integer.parseInt(tfAno.getText()));
            v.setNr_placa(tfPlaca.getText());
            v.setNr_passageiros(Integer.parseInt(tfBancos.getText()));
            veiculoDAO.save(v);
        } else {
            JOptionPane.showMessageDialog(null, "Favor preencher devidamente todos os campos");
        }
        carregaTable(veiculoDAO.getAll());
    }

    private void Alterar() throws SQLException {
        Veiculo f = new Veiculo() {
        };
        try {
            f = veiculoDAO.getById((int) tabela.getValueAt(tabela.getSelectedRow(), 0));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Favor Selecionar um item da tabela");
            ex.printStackTrace();
        }

        f.setNr_ano(Integer.parseInt(tfAno.getText()));
        f.setNr_placa(tfPlaca.getText());
        f.setNr_passageiros(Integer.parseInt(tfBancos.getText()));

        veiculoDAO.update(f);
        carregaTable(veiculoDAO.getAll());
        limpaCampos();
    }

    private void Remover() throws SQLException {
        int codigoRemover = (int) tabela.getValueAt(tabela.getSelectedRow(), 0);
        try {
            veiculoDAO.delete(codigoRemover);
            carregaTable(veiculoDAO.getAll());
            JOptionPane.showMessageDialog(null, "Registro Removido Com Sucesso!!!");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        carregaTable(veiculoDAO.getAll());
        limpaCampos();
    }

    private void carregaTable(List<Veiculo> all) throws SQLException {
        if (all == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setRowCount(0);
        for (Veiculo f : all) {
            model.addRow(new Object[]{f.getCodigo(), f.getNr_ano(), f.getNr_placa(), f.getNr_passageiros()});
        }
        UltimoID();
    }

    private void UltimoID() throws SQLException {
        jLabel4.setText("Codigo do Funcionario #" + veiculoDAO.getLastId());
    }

    private void limpaCampos() {
        tfAno.setText("");
        tfBancos.setText("");
        tfPlaca.setText("");
    }

    private void verificaCampos() {
        if (tfAno.getText().trim().length() > 0 && tfPlaca.getText().trim().length() > 0 && tfBancos.getText().trim().length() > 0) {
            btAdicionar.setEnabled(true);
        } else {
            btAdicionar.setEnabled(false);
        }
        if (tabela.getSelectedRow() < 0) {
            btAlterar.setEnabled(false);
            btRemover.setEnabled(false);
        } else {
            btAlterar.setEnabled(true);
            btRemover.setEnabled(true);
        }

    }
